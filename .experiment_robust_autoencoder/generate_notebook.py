                "class LSTMEncoder(nn.Module):\n",
                "    def __init__(self, input_dim, hidden_dim, num_layers, dropout=0.0):\n",
                "        super().__init__()\n",
                "        self.lstm = nn.LSTM(input_dim, hidden_dim, num_layers, batch_first=True, dropout=dropout if num_layers > 1 else 0.0)\n",
                "    def forward(self, x):\n",
                "        _, (hidden, cell) = self.lstm(x)\n",
                "        return hidden, cell\n",
                "\n",
                "class LSTMDecoder(nn.Module):\n",
                "    def __init__(self, input_dim, hidden_dim, output_dim, num_layers, dropout=0.0):\n",
                "        super().__init__()\n",
                "        self.lstm = nn.LSTM(input_dim, hidden_dim, num_layers, batch_first=True, dropout=dropout if num_layers > 1 else 0.0)\n",
                "        self.output_layer = nn.Linear(hidden_dim, output_dim)\n",
                "    def forward(self, x, hidden, cell):\n",
                "        output, _ = self.lstm(x, (hidden, cell))\n",
                "        return self.output_layer(output)\n",
                "\n",
                "class LSTMAutoencoder(nn.Module):\n",
                "    def __init__(self, input_dim, hidden_dim, latent_dim, num_layers, dropout=0.0):\n",
                "        super().__init__()\n",
                "        self.encoder = LSTMEncoder(input_dim, hidden_dim, num_layers, dropout)\n",
                "        self.decoder = LSTMDecoder(latent_dim, hidden_dim, input_dim, num_layers, dropout)\n",
                "        self.hidden_to_latent = nn.Linear(hidden_dim, latent_dim)\n",
                "    def forward(self, x):\n",
                "        batch_size, seq_len, _ = x.shape\n",
                "        hidden, cell = self.encoder(x)\n",
                "        latent = self.hidden_to_latent(hidden[-1])\n",
                "        decoder_input = latent.unsqueeze(1).repeat(1, seq_len, 1)\n",
                "        reconstruction = self.decoder(decoder_input, hidden, cell)\n",
                "        return reconstruction, latent\n",
                "\n",
                "model = LSTMAutoencoder(INPUT_DIM, HIDDEN_DIM, LATENT_DIM, NUM_LAYERS, DROPOUT).to(device)\n",
                "print(model)"
